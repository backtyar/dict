# Задача 4
# Напишите программу которая использует словарь,
# который содержит десять имен пользователей и
# соответствующие пароли.При запуске, она запрашивает
# у пользователя имя пользователя и пароль.
# Если имя пользователя отсутствует в словаре,
# программа сообщает, что человек не является
# действительным пользователем системы.
# Если имя пользователя есть в словаре, но пароль
# неверный, программа сообщает, что пароль недействителен.
# Если имя пользователя и пароль верные, программа
# сообщает, что пользователь вошел в систему.

name_person = {'Alan':'1234alan', 'Mark':'1234mark', 'Tom':'1234tom',
               'Jess':'1234jess', 'Alis':'1234alis', 'Pit':'1234pit',
               'Jina':'1234jina', 'Anna':'1234anna','Rouse':'1234rouse',
               'Sam':'1234sam',}

while True:
    name = input('name: ').strip()
    password = input('password: ').strip()
    if name not in name_person:
        print('Вы не является действительным пользователем системы')
        continue
    elif name in name_person and password != name_person[name]:
        print('пароль неверный')
        continue
    else:
        print('Вы успешно вошли в систему')
        break






