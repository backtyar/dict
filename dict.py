# Задача 1
# Напишите программу, которая постоянно просит
# пользователя ввести названия продуктов и цены.
# Эта программа предназначена для создания словаря
# продуктов и их цен. Она будет запрашивать у
# пользователя названия продуктов и цены и сохранять
# их в словаре, где названия продуктов будут ключами,
# а цены - значениями.После того, как пользователь
# закончит вводить продукты и цены, программа будет
# позволять им повторно вводить названия продуктов и
# выводить соответствующую цену или сообщение,
# если товара нет в словаре.

list_product = []
while True:
    set_product = input('Укажите прдукт и цену: ').split()
    if 'stop' in set_product:
        break
    elif len(set_product) < 2:
        print('Провильно укажите данные ПРИМЕР: яблока 100')
        continue
    list_product.append(set_product)

dict_product = dict(list_product)
get_product = input('Укажите называние товара и вы узнаете цену товара: ').strip()

print(dict_product.get(get_product,'К сожелению товар с таким нозыванием, не сушествует нашам оссартементе'))






